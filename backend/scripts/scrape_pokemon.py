async def scrape_single_pokemon(
    client: httpx.AsyncClient,
    name: str,
    use_bulbapedia: bool = True,
) -> Optional[Dict[str, Any]]:
    """Scrape all data for a single Pokemon. Returns None on critical failure."""
    # Fetch base Pokemon data
    poke_data = await get_pokemon_data(client, name)
    if poke_data is None:
        log.error(f"Failed to fetch base data for {name}")
        return None

    pokemon_id = poke_data["id"]
    english_name = next(
        (e["name"] for e in poke_data.get("names", []) if e.get("language", {}).get("name") == "en"),
        poke_data.get("name", name).title(),
    )
    # PokéAPI base endpoint gives 'name' not 'names'; species endpoint has names
    if not english_name:
        english_name = poke_data.get("name", name).replace("-", " ").title()
    else:
        english_name = poke_data.get("name", name).replace("-", " ").title()

    # Fetch species data (generation, category, evo chain URL)
    species_data = await get_pokemon_species(client, str(pokemon_id))
    if species_data is None:
        log.warning(f"[{english_name}] No species data, using placeholders")
        species_data = {}

    generation = get_generation(species_data)
    category = get_english_category(species_data)

    # Types
    types = [t["type"]["name"].capitalize() for t in poke_data.get("types", [])]
    type1 = types[0] if types else "Normal"
    type2 = types[1] if len(types) > 1 else None

    # Evolution line
    evo_ids = [pokemon_id]
    evo_url = species_data.get("evolution_chain", {}).get("url")
    if evo_url:
        evo_chain = await get_evolution_chain(client, evo_url)
        if evo_chain:
            evo_ids = extract_evolution_ids(evo_chain)

    # Sprite
    sprites = poke_data.get("sprites", {})
    sprite_url = (
        sprites.get("front_default")
        or sprites.get("other", {}).get("official-artwork", {}).get("front_default")
        or f"https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/{pokemon_id}.png"
    )

    # Chinese name
    name_zh = ""
    name_zh_simplified = ""

    if use_bulbapedia:
        # Use English name as it appears in the game (properly capitalized)
        en_name_for_bulba = english_name
        name_zh, name_zh_simplified = await get_chinese_name_from_bulbapedia(
            client, en_name_for_bulba
        )

    # Also try Chinese name from species data (if available)
    if not name_zh:
        for name_entry in species_data.get("names", []):
            lang = name_entry.get("language", {}).get("name", "")
            if lang == "zh-Hant":
                name_zh = name_entry["name"]
            elif lang == "zh-Hans":
                name_zh_simplified = name_entry["name"]

    if not name_zh:
        log.warning(f"[{english_name}] No Chinese name found, using placeholder")
        name_zh = english_name  # Fallback to English
        name_zh_simplified = english_name

    if not name_zh_simplified:
        name_zh_simplified = _traditional_to_simplified_heuristic(name_zh)

    # Pinyin
    pinyin_tones = get_pinyin_tones(name_zh)
    pinyin_numbered = get_pinyin_numbered(name_zh)
    ipa = get_ipa(pinyin_numbered)

    # Etymology
    etymology = get_etymology(name_zh)

    return {
        "id": pokemon_id,
        "name_en": english_name,
        "name_zh": name_zh,
        "name_zh_simplified": name_zh_simplified,
        "pinyin": pinyin_tones,
        "pinyin_numbered": pinyin_numbered,
        "ipa": ipa,
        "etymology": etymology,
        "generation": generation,
        "type1": type1,
        "type2": type2,
        "category": category,
        "evolution_line": evo_ids,
        "sprite_url": sprite_url or "",
        "audio_filename": None,
    }


async def run_scraper(limit: int = 1025, resume: bool = True, use_bulbapedia: bool = True) -> None:
    _CACHE_DIR.mkdir(parents=True, exist_ok=True)
    (_CACHE_DIR / "pokemon").mkdir(exist_ok=True)
    (_CACHE_DIR / "species").mkdir(exist_ok=True)
    (_CACHE_DIR / "evolution").mkdir(exist_ok=True)
    (_CACHE_DIR / "bulbapedia").mkdir(exist_ok=True)

    # Load existing results if resuming
    existing_results: Dict[int, Dict] = {}
    if resume and _OUTPUT_JSON.exists():
        with open(_OUTPUT_JSON, "r", encoding="utf-8") as f:
            existing_list = json.load(f)
            existing_results = {p["id"]: p for p in existing_list}
        log.info(f"[resume] Loaded {len(existing_results)} existing Pokemon from {_OUTPUT_JSON}")

    headers = {
        "User-Agent": "Pokenese-Scraper/1.0 (educational project; contact via GitHub)",
    }

    async with httpx.AsyncClient(headers=headers) as client:
        # Get full Pokemon list
        log.info("[fetch] Getting Pokemon list from PokéAPI...")
        pokemon_list = await get_all_pokemon_list(client, limit=limit)
        log.info(f"[fetch] Found {len(pokemon_list)} Pokemon to process")

        results: List[Dict] = []
        failed: List[str] = []

        for i, poke_ref in enumerate(pokemon_list):
            poke_name = poke_ref["name"]

            # Extract ID from URL for quick lookup
            url_parts = poke_ref["url"].rstrip("/").split("/")
            try:
                poke_id = int(url_parts[-1])
            except ValueError:
                poke_id = -1

            # Skip if already scraped (resume mode)
            if resume and poke_id in existing_results:
                results.append(existing_results[poke_id])
                continue

            log.info(f"[{i+1}/{len(pokemon_list)}] Scraping {poke_name} (ID: {poke_id})...")

            try:
                data = await scrape_single_pokemon(client, poke_name, use_bulbapedia)
                if data:
                    results.append(data)
                    log.info(f"  ✓ {data['name_en']} | zh: {data['name_zh']} | {data['type1']}")
                else:
                    failed.append(poke_name)
                    log.error(f"  ✗ Failed: {poke_name}")
            except Exception as e:
                failed.append(poke_name)
                log.error(f"  ✗ Exception for {poke_name}: {e}")

            # Save progress every 50 Pokemon
            if len(results) % 50 == 0 and results:
                _write_json(results)
                log.info(f"[checkpoint] Saved {len(results)} Pokemon to {_OUTPUT_JSON}")

    # Sort by ID
    results.sort(key=lambda x: x["id"])

    # Final write
    _write_json(results)
    _write_typescript(results)

    log.info(f"\n[done] Scraped {len(results)} Pokemon")
    if failed:
        log.warning(f"[done] Failed for {len(failed)} Pokemon: {', '.join(failed[:20])}")
    log.info(f"[done] JSON → {_OUTPUT_JSON}")
    log.info(f"[done] TypeScript → {_OUTPUT_TS}")


def _write_json(results: List[Dict]) -> None:
    _OUTPUT_JSON.parent.mkdir(parents=True, exist_ok=True)
    with open(_OUTPUT_JSON, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)


def _write_typescript(results: List[Dict]) -> None:
    """Write results as a TypeScript constant file."""
    _OUTPUT_TS.parent.mkdir(parents=True, exist_ok=True)

    ts_lines = [
        "// Generated by scripts/scrape_pokemon.py",
        "// Do not edit directly — regenerate with: python backend/scripts/scrape_pokemon.py",
        "",
        'import type { PokemonType } from "@/types/pokemon";',
        "",
        "export const POKEMON_DATA: PokemonType[] = ",
    ]

    # Serialize to JSON then embed as TS value
    json_str = json.dumps(results, ensure_ascii=False, indent=2)
    ts_lines.append(json_str + ";")

    with open(_OUTPUT_TS, "w", encoding="utf-8") as f:
        f.write("\n".join(ts_lines) + "\n")


