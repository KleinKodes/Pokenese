def get_pinyin_tones(chinese: str) -> str:
    """Return pinyin with tone marks, e.g. 'Pēn Huǒ Lóng'."""
    if not chinese or not HAS_PYPINYIN:
        return ""
    try:
        result = pinyin(chinese, style=Style.TONE)
        return " ".join(item[0].capitalize() if i == 0 else item[0] for i, item in enumerate(result))
    except Exception:
        return ""


def get_pinyin_numbered(chinese: str) -> str:
    """Return pinyin with numbered tones, e.g. 'Pen1 Huo3 Long2'."""
    if not chinese or not HAS_PYPINYIN:
        return ""
    try:
        result = pinyin(chinese, style=Style.TONE3)
        return " ".join(item[0].capitalize() if i == 0 else item[0] for i, item in enumerate(result))
    except Exception:
        return ""


def get_ipa(pinyin_str: str) -> str:
    """Convert numbered pinyin to IPA using dragonmapper."""
    if not pinyin_str or not HAS_DRAGONMAPPER:
        return ""
    try:
        # dragonmapper expects numbered pinyin without spaces between syllables
        # Process syllable by syllable
        syllables = pinyin_str.split()
        ipa_parts = []
        for syl in syllables:
            try:
                ipa = transcriptions.pinyin_to_ipa(syl)
                ipa_parts.append(ipa)
            except Exception:
                ipa_parts.append(syl)
        return "/" + " ".join(ipa_parts) + "/"
    except Exception:
        return ""


def get_etymology(chinese: str) -> List[Dict[str, str]]:
    """Build etymology breakdown for each character."""
    if not chinese or not HAS_PYPINYIN:
        return []
    entries = []
    for char in chinese:
        if not char.strip():
            continue
        try:
            char_pinyin = pinyin(char, style=Style.TONE)[0][0]
        except Exception:
            char_pinyin = ""
        entries.append({
            "character": char,
            "pinyin": char_pinyin,
            "meaning": "",  # Meaning requires a dictionary lookup; left blank for scraper
        })
    return entries


