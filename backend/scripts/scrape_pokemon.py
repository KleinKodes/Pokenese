async def get_chinese_name_from_bulbapedia(
    client: httpx.AsyncClient,
    english_name: str,
) -> Tuple[str, str]:
    """
    Scrape traditional and simplified Chinese names from Bulbapedia.
    Returns (traditional_zh, simplified_zh) or ("", "") on failure.
    """
    cache = _CACHE_DIR / "bulbapedia" / f"{english_name.lower()}.json"
    if cache.exists():
        with open(cache, "r", encoding="utf-8") as f:
            data = json.load(f)
            return data.get("traditional", ""), data.get("simplified", "")

    # Bulbapedia URL uses underscores for spaces
    page_name = english_name.replace(" ", "_")
    url = f"{BULBAPEDIA_BASE}/{page_name}_(Pok%C3%A9mon)"

    try:
        await asyncio.sleep(REQUEST_DELAY)
        resp = await client.get(url, timeout=20.0, follow_redirects=True)
        if resp.status_code != 200:
            return "", ""

        soup = BeautifulSoup(resp.text, "lxml")

        traditional = ""
        simplified = ""

        # Look for Chinese name in the infobox table
        # Bulbapedia typically has a table with language names
        # The Chinese name section usually has 漢字 / 中文 labels

        # Strategy 1: Find "Chinese" label in name tables
        for td in soup.find_all("td"):
            text = td.get_text(strip=True)
            # Traditional Chinese (Taiwan) is usually listed as 繁體中文 or just Chinese
            if "中文" in text or "漢字" in text or "Chinese" in text:
                # Get sibling or next td
                next_td = td.find_next_sibling("td")
                if next_td:
                    zh = next_td.get_text(strip=True)
                    if zh and _is_chinese(zh):
                        traditional = zh
                        break

        # Strategy 2: Look in the infobox for zh name entries
        if not traditional:
            # Find all spans/divs that might contain Chinese characters
            for tag in soup.find_all(["span", "td", "th"]):
                text = tag.get_text(strip=True)
                if _is_chinese(text) and 2 <= len(text) <= 8:
                    # Plausible Pokemon name length in Chinese
                    traditional = text
                    break

        # For simplified: most names differ only slightly; use traditional as fallback
        # In practice, Bulbapedia often lists both
        if traditional and not simplified:
            simplified = _traditional_to_simplified_heuristic(traditional)

        result = {"traditional": traditional, "simplified": simplified}
        cache.parent.mkdir(parents=True, exist_ok=True)
        with open(cache, "w", encoding="utf-8") as f:
            json.dump(result, f, ensure_ascii=False)

        return traditional, simplified

    except Exception as e:
        log.debug(f"Bulbapedia fetch failed for {english_name}: {e}")
        return "", ""


def _is_chinese(text: str) -> bool:
    """Check if text contains predominantly CJK characters."""
    if not text:
        return False
    cjk_count = sum(1 for c in text if "\u4e00" <= c <= "\u9fff")
    return cjk_count / len(text) > 0.5


def _traditional_to_simplified_heuristic(traditional: str) -> str:
    """
    Very basic traditional→simplified substitution for common Pokemon name characters.
    For production, use opencc or a full conversion table.
    """
    TRAD_TO_SIMP = {
        "龍": "龙", "獸": "兽", "龜": "龟", "夢": "梦", "幻": "幻",
        "飛": "飞", "電": "电", "蟲": "虫", "鳥": "鸟", "魚": "鱼",
        "貓": "猫", "狗": "狗", "馬": "马", "鳳": "凤", "鶴": "鹤",
        "鬥": "斗", "發": "发", "愛": "爱", "來": "来", "風": "风",
        "陽": "阳", "時": "时", "國": "国", "進": "进", "種": "种",
        "輝": "辉", "遊": "游", "傑": "杰", "聲": "声", "護": "护",
        "鐵": "铁", "會": "会", "樹": "树", "聯": "联", "實": "实",
        "裂": "裂", "螢": "萤", "燈": "灯", "雷": "雷", "閃": "闪",
    }
    result = []
    for char in traditional:
        result.append(TRAD_TO_SIMP.get(char, char))
    return "".join(result)


