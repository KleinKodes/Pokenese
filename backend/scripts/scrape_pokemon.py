def main() -> None:
    parser = argparse.ArgumentParser(description="Scrape Pokemon data")
    parser.add_argument("--limit", type=int, default=1025, help="Max Pokemon to scrape")
    parser.add_argument(
        "--no-resume", action="store_true", help="Re-scrape everything (ignore cache)"
    )
    parser.add_argument(
        "--no-bulbapedia", action="store_true", help="Skip Bulbapedia scraping"
    )
    args = parser.parse_args()

    asyncio.run(
        run_scraper(
            limit=args.limit,
            resume=not args.no_resume,
            use_bulbapedia=not args.no_bulbapedia,
        )
    )


if __name__ == "__main__":
    main()
