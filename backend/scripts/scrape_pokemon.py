async def fetch_json(client: httpx.AsyncClient, url: str, cache_path: Optional[Path] = None) -> Optional[Dict]:
    """Fetch JSON from URL, using file cache if provided."""
    if cache_path and cache_path.exists():
        with open(cache_path, "r", encoding="utf-8") as f:
            return json.load(f)

    try:
        await asyncio.sleep(REQUEST_DELAY)
        resp = await client.get(url, timeout=15.0)
        resp.raise_for_status()
        data = resp.json()
        if cache_path:
            cache_path.parent.mkdir(parents=True, exist_ok=True)
            with open(cache_path, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False)
        return data
    except Exception as e:
        log.warning(f"Failed to fetch {url}: {e}")
        return None


async def get_all_pokemon_list(client: httpx.AsyncClient, limit: int = 1025) -> List[Dict]:
    """Fetch paginated list of all Pokemon from PokéAPI."""
    url = f"{POKEAPI_BASE}/pokemon?limit={limit}&offset=0"
    data = await fetch_json(client, url, _CACHE_DIR / "pokemon_list.json")
    if data is None:
        return []
    return data.get("results", [])


async def get_pokemon_data(client: httpx.AsyncClient, name_or_id: str) -> Optional[Dict]:
    cache = _CACHE_DIR / "pokemon" / f"{name_or_id}.json"
    url = f"{POKEAPI_BASE}/pokemon/{name_or_id}"
    return await fetch_json(client, url, cache)


async def get_pokemon_species(client: httpx.AsyncClient, name_or_id: str) -> Optional[Dict]:
    cache = _CACHE_DIR / "species" / f"{name_or_id}.json"
    url = f"{POKEAPI_BASE}/pokemon-species/{name_or_id}"
    return await fetch_json(client, url, cache)


async def get_evolution_chain(client: httpx.AsyncClient, url: str) -> Optional[Dict]:
    # Extract chain ID from URL for caching
    chain_id = url.rstrip("/").split("/")[-1]
    cache = _CACHE_DIR / "evolution" / f"{chain_id}.json"
    return await fetch_json(client, url, cache)


def extract_evolution_ids(chain: Dict) -> List[int]:
    """Recursively extract all Pokemon IDs from an evolution chain."""
    ids = []

    def _traverse(node: Dict) -> None:
        species_url = node.get("species", {}).get("url", "")
        match = re.search(r"/pokemon-species/(\d+)/", species_url)
        if match:
            ids.append(int(match.group(1)))
        for evo in node.get("evolves_to", []):
            _traverse(evo)

    _traverse(chain.get("chain", {}))
    return ids


def get_english_category(species_data: Dict) -> str:
    """Extract English Pokédex category (e.g. 'Flame Pokémon')."""
    for entry in species_data.get("genera", []):
        if entry.get("language", {}).get("name") == "en":
            return entry["genus"]
    return "Unknown Pokémon"


def get_generation(species_data: Dict) -> int:
    gen_name = species_data.get("generation", {}).get("name", "generation-i")
    return GENERATION_MAP.get(gen_name, 1)


